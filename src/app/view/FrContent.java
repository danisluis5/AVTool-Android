    private Double getCapDoBeTong(String capDoBeTong) {
        switch (capDoBeTong) {
            case "B15":
                return 8.5;
            case "B20":
                return 11.5;
            case "B25":
                return 14.5;
            case "B30":
                return 17.0;
            case "B35":
                return 19.5;
            case "B40":
                return 22.0;
            case "B45":
                return 25.0;
        }
        return 0.0;
    }
    
    private Double getMacThep(String macThep) {
        switch (macThep) {
            case "AII":
                return 280.0;
            case "AIII":
                return 365.0;
        }
        return 0.0;
    }
