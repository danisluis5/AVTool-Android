    private void performChieuTaiVL(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_performChieuTaiVL

        Double phi = 1.0;
        Double m1 = 0.85, m2 = 0.7;
        Double Ra = getMacThep(cbMacThep.getSelectedItem().toString()) * 1000.0;
        Double Rb = getCapDoBeTong(cbCapBeTong.getSelectedItem().toString()) * 1000.0;
        // Coc Ep
        Double Fa = Double.parseDouble(tfSoThanhThep.getText())*Math.PI*Double.parseDouble(cbDuongKinhThep.getSelectedItem().toString())*Double.parseDouble(cbDuongKinhThep.getSelectedItem().toString())/(4.0*1000000.0);
        
        Double Fb = 0.0;
        if (rdCocEp.isSelected()) {
            Fb = Double.parseDouble(tfA1.getText())*Double.parseDouble(tfA1.getText())/(1000000.0) - Fa;
        } else {
            Fb = Math.PI*Double.parseDouble(cbDuongKinhThep.getSelectedItem().toString())*Double.parseDouble(cbDuongKinhThep.getSelectedItem().toString())/(4.0*1000000.0) - Fa;
        }   
        Double result = phi*(m1*m2*Rb*Fb + Ra*Fa);
        tfSucChieTaiVatLieu.setText(String.format("%.2f", result));
    }//GEN-LAST:event_performChieuTaiVL

